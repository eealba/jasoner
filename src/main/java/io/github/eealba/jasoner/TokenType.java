package io.github.eealba.jasoner;

/**
 * The enum Token type.
 */
public enum TokenType {
    /**
     * Object start token type.
     */
    OBJECT_START,
    /**
     * Object end token type.
     */
    OBJECT_END,
    /**
     * Array start token type.
     */
    ARRAY_START,
    /**
     * Array end token type.
     */
    ARRAY_END,
    /**
     * True token type.
     */
    TRUE,
    /**
     * False token type.
     */
    FALSE,
    /**
     * Null token type.
     */
    NULL,
    /**
     * Comma token type.
     */
    COMMA,
    /**
     * Colon token type.
     */
    COLON,
    /**
     * Text token type.
     */
    TEXT,
    /**
     * Number token type.
     */
    NUMBER;

}
